package com.techlabs.sets;

import java.util.HashSet;
import java.util.Set;

public class HashSetDemo {

	public static void main(String[] args) {
		
		Set<String> set=new HashSet<String>();
		
		set.add("Saurabh");
		set.add("Sanajy");
		set.add("Ramya");
		set.add("Neha");
		
		System.out.println(set);

	}

}
